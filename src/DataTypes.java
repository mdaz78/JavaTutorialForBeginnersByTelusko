public class DataTypes {
  public static void main(String args[]) {
    // primitive data types
    int i = 5;
    byte b = 7;
    short sh = 77;
    long l = 20202l;

    float f = 66.6f;
    double d = 0.09;

    // print
    System.out.println(i);
    System.out.println(b);
    System.out.println(sh);
    System.out.println(l);
    System.out.println(f);
    System.out.println(d);

  }
}
